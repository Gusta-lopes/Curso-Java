public class outrosOperadores {
    public static void main (String[] args){

        int total = 10;
        total += 3; //o += vai somar o valor que se deseja acrescentar a variavel
        total -= 2; //imprime 11
        total *= 3; //imprime 33
        total /= 3; //imprime 11
        total %= 5; //imprime 1
        System.out.println(total);
    }
}
